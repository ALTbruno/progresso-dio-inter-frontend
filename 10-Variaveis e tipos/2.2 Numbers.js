/*
Numbers:
- Declarado sem aspas
- Inteiros ou decimais

*/


let numero = 7;
console.log(numero); // 7
console.log(numero*3); // 21
console.log(numero+3); // 10
console.log(numero-3); // 4
console.log(numero/3); // 2.3333333333333335
console.log(numero%3); // 1 (resto de 7/3)

console.log(Math.floor(numero/3)); // 2 (arredonda a divisão para baixo)
console.log(Math.ceil(numero/3)); // 3 (arredonda a divisão para cima)

